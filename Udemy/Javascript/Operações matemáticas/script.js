/*Na programação existem varias operações de soma, subtração, divisão etc que são cahamadas de operações matemáticas
e não nescessariamente são usando números*/

//vamos de um simples exemplo:
var a = 2;
var b = 3;
//agora vamos criar uma operação e vamos atribuir o valor de a+b
var soma = a+b;
var sub = a-b;
//multiplicação usamos o sinal de "*"
var mult = a*b;
//em divisão usamos o sinal "/"
var div = a/b;
//agora vamos imprimir todos os resultados usando "alert"
alert(soma);
alert(sub);
alert(mult);
alert(div);

//obs: variaveis são iniciadas somente com letras(podem ser o nome que quiser), sem acento, e se tiver números que ainda sim sejam iniciados somente com letras.
